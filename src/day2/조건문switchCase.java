package day2;

public class 조건문switchCase {

	public static void main(String[] args) {
		char c = 'a';
		switch (c) {
		case 'a':
			System.out.println("a이다");
			break;
		case 'b':
			System.out.println("b이다");
			break;
		case 'c':
			System.out.println("c이다");
			break;

		default:
			System.out.println("몰라");
		}
	}

}
